/**
 * Operadores de comparacion
 */

//Operador igual (==)
console.log(3==3);//muestra un valor booleano
console.log(3=='3');

//Operadores desigual (!=)
console.log(3!=3);

//Operador estrictamente igual (===)
console.log(3===3);
console.log(3==='3');

//Operador de desigualdad estricta (!==)
console.log(3!=='3');

//Operadores mayores...
console.log(3>4);
console.log(3>=3);
console.log(2<4);
console.log(2<=2);
